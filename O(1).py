def add_items(n):
    return n + n + n

print(add_items(10))
#Output: 30
#Explanation:   The function add_items takes a single integer n as input and returns the sum of n added to itself three times, which is equivalent to 3 * n. When the function is called with the argument 10, it computes 10 + 10 + 10, resulting in 30.
#Time Complexity: O(1)
#Space Complexity: O(1)